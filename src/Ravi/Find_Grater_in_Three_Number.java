package Ravi;

public class Find_Grater_in_Three_Number
{

	public static void main(String[] args)
	{
		int a=20;
		int b=22;
		int c=25;
		
		if(a>b && a>c)
		{
			System.out.println(a+" a is grater number");
		}
		else if(b>a && b>c)
		{
			System.out.println(b +" b is grater number");
		}
		else
		{
			System.out.println(c+" c is grater number");
		}
	}

}
